from src.data.prepare_dataset import build_sql_string


def normalize_result(result: list) -> list:
    """Sort and stringify results for comparison."""
    return sorted([str(row) for row in result])


def evaluate_single(
    predicted_sql: str,
    gold_sql: dict,
    table: dict,
) -> dict:
    """
    Evaluate a single prediction.

    Args:
        predicted_sql: SQL string generated by the model
        gold_sql: the WikiSQL sql dict (with sel, agg, conds)
        table: the WikiSQL table dict

    Returns:
        dict with keys: correct, syntax_error, error_message
    """
    # Build gold SQL using our formatter
    gold_sql_str = build_sql_string(gold_sql, table["header"], table["types"])

    # Execute both
    pred_results, pred_error = execute_sql(table, predicted_sql)
    gold_results, gold_error = execute_sql(table, gold_sql_str)

    if pred_error:
        return {"correct": False, "syntax_error": True, "error_message": pred_error}

    correct = normalize_result(pred_results) == normalize_result(gold_results)
    return {"correct": correct, "syntax_error": False, "error_message": None}


def evaluate_dataset(predictions: list[str], dataset: list[dict]) -> dict:
    """
    Evaluate a list of predicted SQL strings against the dataset.

    Args:
        predictions: list of predicted SQL strings
        dataset: list of WikiSQL examples (with sql and table fields)

    Returns:
        dict with overall metrics
    """
    total = len(predictions)
    correct = 0
    syntax_errors = 0

    for pred, example in zip(predictions, dataset):
        result = evaluate_single(pred, example["sql"], example["table"])
        if result["correct"]:
            correct += 1
        if result["syntax_error"]:
            syntax_errors += 1

    return {
        "execution_accuracy": correct / total,
        "syntax_error_rate": syntax_errors / total,
        "total": total,
        "correct": correct,
        "syntax_errors": syntax_errors,
    }


